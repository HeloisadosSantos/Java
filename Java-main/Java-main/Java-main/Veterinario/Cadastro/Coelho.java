package Cadastro;

public class Coelho extends Animal{

      //caracteristica
    String cor;
    String peso;
    String silvestre;

    public String getCor() {
        return cor;
    }
    public void setCor(String cor) {
        this.cor = cor;
    }
    public String getPeso() {
        return peso;
    }
    public void setPeso(String peso) {
        this.peso = peso;
    }
    public String getSilvestre() {
        return silvestre;
    }
    public void setSilvestre(String silvestre) {
        this.silvestre = silvestre;
    }
}

